mark = int(input("Enter Your Exam Number :"))

if mark >= 80:
    print("A+")

elif mark >= 70:
    print("A")

elif mark >= 60:
    print("A-")

elif mark >= 50:
    print("B")

elif mark >= 40:
    print("Your Exam Grade Is :C")

elif mark >= 33:
    print("D")


else:
    print("Sorry.You Failed")

print("Thank You")